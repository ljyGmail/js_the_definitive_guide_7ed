// 4.1 Primary Expressions

// Literals
console.log(`1.23: ${1.23}`); // A number literal
console.log(`"hello": ${"hello"}`); // A string literal
console.log(`/pattern/: ${/pattern/}`); // A regular expression literal

// Reserved words
console.log(`true: ${true}`); // Evaluates to the boolean true value
console.log(`false: ${false}`); // Evaluates to the boolean false value
console.log(`null: ${null}`); // Evaluates to the null value
console.log(`this: ${this}`); // Evaluates to the "current" object

// a reference to a variable
// i // Evaluates to the value of the variable i.
// sum // Evaluates to the value of the variable sum.
// undefined // The value of the "undefined" property of the global object.
